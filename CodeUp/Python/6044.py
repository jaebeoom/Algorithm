num1, num2 = map(int, input().split())

plus_result = num1 + num2
minus_result = num1 - num2
multiple_result = num1 * num2
quotient_result = num1 // num2
remainder_result = num1 % num2
division_result = format(num1 / num2, '.2f')

print(plus_result)
print(minus_result)
print(multiple_result)
print(quotient_result)
print(remainder_result)
print(division_result)